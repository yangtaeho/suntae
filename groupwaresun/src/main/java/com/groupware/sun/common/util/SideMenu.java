package com.groupware.sun.common.util;

import java.io.Serializable;

public class SideMenu implements Serializable {

	private static final long serialVersionUID = 1L;
	private int side_menu_no;
	private int side_menu_branch_no;
	private String side_menu_branch_name;
	private int side_menu_branch_role_no;
	private int side_menu_terminal_no;
	private String side_menu_terminal_name;
	private int side_menu_terminal_role_no;

	public SideMenu() {
	}

	public SideMenu(int side_menu_no, int side_menu_branch_no, String side_menu_branch_name,
			int side_menu_branch_role_no, int side_menu_terminal_no, String side_menu_terminal_name,
			int side_menu_terminal_role_no) {
		super();
		this.side_menu_no = side_menu_no;
		this.side_menu_branch_no = side_menu_branch_no;
		this.side_menu_branch_name = side_menu_branch_name;
		this.side_menu_branch_role_no = side_menu_branch_role_no;
		this.side_menu_terminal_no = side_menu_terminal_no;
		this.side_menu_terminal_name = side_menu_terminal_name;
		this.side_menu_terminal_role_no = side_menu_terminal_role_no;
	}

	public int getSide_menu_no() {
		return side_menu_no;
	}

	public void setSide_menu_no(int side_menu_no) {
		this.side_menu_no = side_menu_no;
	}

	public int getSide_menu_branch_no() {
		return side_menu_branch_no;
	}

	public void setSide_menu_branch_no(int side_menu_branch_no) {
		this.side_menu_branch_no = side_menu_branch_no;
	}

	public String getSide_menu_branch_name() {
		return side_menu_branch_name;
	}

	public void setSide_menu_branch_name(String side_menu_branch_name) {
		this.side_menu_branch_name = side_menu_branch_name;
	}

	public int getSide_menu_branch_role_no() {
		return side_menu_branch_role_no;
	}

	public void setSide_menu_branch_role_no(int side_menu_branch_role_no) {
		this.side_menu_branch_role_no = side_menu_branch_role_no;
	}

	public int getSide_menu_terminal_no() {
		return side_menu_terminal_no;
	}

	public void setSide_menu_terminal_no(int side_menu_terminal_no) {
		this.side_menu_terminal_no = side_menu_terminal_no;
	}

	public String getSide_menu_terminal_name() {
		return side_menu_terminal_name;
	}

	public void setSide_menu_terminal_name(String side_menu_terminal_name) {
		this.side_menu_terminal_name = side_menu_terminal_name;
	}

	public int getSide_menu_terminal_role_no() {
		return side_menu_terminal_role_no;
	}

	public void setSide_menu_terminal_role_no(int side_menu_terminal_role_no) {
		this.side_menu_terminal_role_no = side_menu_terminal_role_no;
	}

	@Override
	public String toString() {
		return "SideMenu [side_menu_no=" + side_menu_no + ", side_menu_branch_no=" + side_menu_branch_no
				+ ", side_menu_branch_name=" + side_menu_branch_name + ", side_menu_branch_role_no="
				+ side_menu_branch_role_no + ", side_menu_terminal_no=" + side_menu_terminal_no
				+ ", side_menu_terminal_name=" + side_menu_terminal_name + ", side_menu_terminal_role_no="
				+ side_menu_terminal_role_no + "]";
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + side_menu_no;
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		SideMenu other = (SideMenu) obj;
		if (side_menu_no != other.side_menu_no)
			return false;
		return true;
	}

}
